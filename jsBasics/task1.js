// Створіть програму, в якій ви оголосите змінні для кожного з примітивних типів даних: 
// рядок, число, буль, null та undefined. 
// Присвойте їм значення та виведіть кожну змінну на консоль.

// Рядок (String)
const myString = "hello world!";
console.log("String:", myString);

// Число (Number)
const myNumber = 42;
console.log("Number:", myNumber);

// Булеве значення (Boolean)
const isBoolean = true;
console.log("Boolean:", isBoolean);

// Значення null
const nullValue = null;
console.log("Null:", nullValue);

// Значення undefined
let undefinedValue;
console.log("Undefined:", undefinedValue);